export function getIcon(name: IconName, className: string = 'w-5 h-5'): string {
  const path = icons[name];
  if (!path) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }
  return `<svg class="${className}" fill="none" stroke="currentColor" viewBox="0 0 24 24">${path}</svg>`;
}

// Helper to create icon elements dynamically
export function createIconElement(name: IconName, className: string = 'w-5 h-5'): SVGSVGElement {
  const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
  svg.setAttribute('class', className);
  svg.setAttribute('fill', 'none');
  svg.setAttribute('stroke', 'currentColor');
  svg.setAttribute('viewBox', '0 0 24 24');
  svg.innerHTML = icons[name] || '';
  return svg;
}
